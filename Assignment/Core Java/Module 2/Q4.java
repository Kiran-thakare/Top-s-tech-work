/*
 * Q4. Write a program in Java to display the first 10 natural numbers 
   using while loop. 
 */

import java.util.Scanner;

public class Q4 {

	public static void main(String[] args) {

		int num = 1;
		System.out.println("First 10 Natural Numbers ");
		while (num <= 10) {
			System.out.println(num);
			num++;
		}

	}
}

