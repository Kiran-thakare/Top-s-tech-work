/*
 * W.A.J.P to get the character at the given index within the String. Original String = 
Tops Technologies! The character at position 0 is T, The character at position 10 is 
o 
 */
public class Q14 {
   public static void main(String[] args) {
	
	   String s1="Tops Technologies";
	   
	   int a=s1.charAt(0);
	   int b=s1.charAt(10);
	   
	   System.out.println("The Character At Position 0:"+(char)a);
	   System.out.println("The Character At Position 10:"+(char)b);
}
}
